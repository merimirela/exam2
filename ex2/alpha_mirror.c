#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;

	if (argc == 2)
	{
		i = 0;
		while (argv[1][i])
		{
			if (argv[1][i] >= 65 && argv[1][i] <= 90) // A to Z
			{
				argv[1][i] = 90 - argv[1][i] + 65;
				//         90 - 65(A)    + 65 = 90 (Z)
				//         90 - 90(Z)    + 65 = 65 (A)
				//         90 - 77(M)    + 65 = 78 (N)
			}
			if (argv[1][i] >= 97 && argv[1][i] <= 122) // a to z
			{
				argv[1][i] = 122 - argv[1][i] + 97;
			}
			write (1, &argv[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}
